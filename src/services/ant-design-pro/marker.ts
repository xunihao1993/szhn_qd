import { request } from 'umi';

/**  数据概要 POST /api/rule */
export async function overviewData(body:any, options?: { [key: string]: any }) {
  return request('/api/marker/overview_data/', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    data: body,
    ...(options || {}),
  });
}

